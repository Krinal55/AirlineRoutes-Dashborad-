--Compare profitability of doestic vs international routes
SELECT 
    CASE 
        WHEN Origin IN ('ORD', 'DFW', 'DEN', 'ATL', 'SFO', 'SEA') 
         AND Destination IN ('ORD', 'DFW', 'DEN', 'ATL', 'SFO', 'SEA') THEN 'Domestic'
        ELSE 'International'
    END AS RouteCategory,
    SUM(Revenue) AS TotalRevenue,
    SUM(OperationalCost) AS TotalCost,
    SUM(Revenue - OperationalCost) AS TotalProfit,
    ROUND(SUM(Revenue - OperationalCost) / SUM(Revenue) * 100, 2) AS ProfitMarginPct
FROM AirlineRoutes
GROUP BY RouteCategory;

